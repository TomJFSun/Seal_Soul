void Display_Refresh(void)
{
	unsigned int i;
	unsigned int line;
	unsigned int bytes;
	unsigned char loop;
	


	
	loop = 0;
	E_INK_WriteCommand(0x10);
again:
	//1:write 30 line w
	line = 30;
	bytes = 400/8*line;
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(0xff);
	}
	//2:write user0 bmp res
	line = 66;
	bytes = 400/8*line;		
	//memset(DisplayBuffer,0xff,bytes);
	Display_WriteBufferRes(0,0,400,67,(unsigned char*)UserRes0);
	UpdateStates();
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(DisplayBuffer[i]);
	}
	//3:write 19 line w
	line = 19;
	bytes = 400/8*line;
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(0xff);
	}
	//4:write 65+65 line result data
	line = 65;
	bytes = 400/8*line;		
	memcpy(DisplayBuffer,UserState1,bytes);
	UpdateFont_A();
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(DisplayBuffer[i]);
	}	
	memcpy(DisplayBuffer,UserState2,bytes);
	UpdateFont_B();
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(DisplayBuffer[i]);
	}		
	//5:write 70 line w
	line = 55;
	bytes = 400/8*line;
	for(i=0;i<bytes;i++)
	{	
		E_INK_WriteData(0xff);
	}	
	if(loop == 0)
	{
		E_INK_WriteCommand(0x13);		
		loop = 1;
		goto again;
	}
	
}


